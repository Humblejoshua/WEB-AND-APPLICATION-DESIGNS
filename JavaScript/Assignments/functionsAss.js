function computation(first_num, second_num, operation){
    switch (operation){
        case 'add':
            console.log(`Addition of ${first_num} and ${second_num} is ${first_num + second_num}`);
            break;
        
        case 'Subtract':
            console.log(`Subtraction of ${first_num} and ${second_num} is ${first_num - second_num}`);
            break;

        case 'multiply':
            console.log(`Multiplication of ${first_num} and ${second_num} is ${first_num * second_num}`);
            break;
            
        case 'Divide':
            console.log(`Division of ${first_num} and ${second_num} is ${first_num / second_num}`);
            break;    

    }
}
computation(2,3,'multiply')